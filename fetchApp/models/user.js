export default class User {
  constructor(id, first_name, last_name, email, address, city, state, zip) {
    this.id = id;
    this.first_name = first_name;
    this.last_name = last_name;
    this.email = email;
    this.address = address;
    this.city = city;
    this.state = state;
    this.zip = zip;
  }
}
